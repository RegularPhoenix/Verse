	local section = db.section
	local config = db.config
	local fortune = require("alpha.fortune")

	if require("userconfig.verse").fortune then
		config.layout = {
			{ type = "padding", val = 2 },
			section.header,
			{ type = "padding", val = 2 },
			section.buttons,
			{
				type = "text",
				val = fortune,
				opts = {
					position = "center",
					hl = "Comment",
					hl_shortcut = "Comment",
				},
			}
		}
	else
		config.layout = {
			{ type = "padding", val = 3 },
			section.header,
			{ type = "padding", val = 2 },
